package cse340.menus.enums;

/**
 * There are three task types to test in each session. This refers to the order of menu items.
 * LINEAR menus have menu items that have an obvious linear order to them
 * RELATIVE menus have menu items that naturally relate to each other
 * UNCLASS menus have randomly related menu items
 */
public enum TaskType {
    LINEAR,
    RELATIVE,
    UNCLASS
}
